VARIABLE SCORE
VARIABLE LAST-ROLL
VARIABLE BONUS
-1 CONSTANT NOTHING

: START
    0 SCORE ! 
    NOTHING LAST-ROLL ! 
    BONUS OFF ;

: COLLECT-BONUS ( #pins -- #pins )
    BONUS @ IF 
        DUP SCORE +!
        BONUS OFF
    THEN ;

: CHECK-SPARE ( #pins -- #pins )
    LAST-ROLL @ DUP NOTHING <> IF
        OVER + 10 = IF BONUS ON THEN
    ELSE DROP THEN ;
    
: ROLL+ ( #pins -- )
    COLLECT-BONUS
    CHECK-SPARE
    LAST-ROLL @ NOTHING = IF DUP ELSE NOTHING THEN 
    LAST-ROLL ! 
    SCORE +! ;
