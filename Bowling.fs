VARIABLE _SCORE
VARIABLE BONUS
VARIABLE LAST-ROLL
VARIABLE OPEN-FRAME

: START-GAME 
    _SCORE OFF 
    BONUS OFF 
    LAST-ROLL OFF 
    OPEN-FRAME OFF ;

: SCORE 
    _SCORE @ ;

: COLLECT-BONUS ( pins -- )
    BONUS @ IF
        _SCORE +!
        BONUS OFF
    ELSE 
        DROP 
    THEN ;

: CALC-BONUS
    LAST-ROLL @ + 10 = 
    OPEN-FRAME @ AND IF
        BONUS ON
    THEN ;

: ADVANCE-FRAME
    OPEN-FRAME @ 0= OPEN-FRAME ! ;

: ADD-ROLL ( pins -- )
    DUP COLLECT-BONUS
    DUP CALC-BONUS
    DUP LAST-ROLL !
    _SCORE +! 
    ADVANCE-FRAME ;
